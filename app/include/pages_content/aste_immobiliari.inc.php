                <div class="panel-body">
                    <p class="JUSTIFIED">
                        <strong>L'avviso di vendita</strong>  è il documento redatto dal professionista delegato, contenente la notizia dell’ordine di vendita emesso dal Giudice, che permette al pubblico di conoscere le informazioni essenziali per poter partecipare all’asta. L’avviso contiene ad esempio:
                    </p>
                    <ul class="MARGIN_LEFT_20">
                        <li>la descrizione del bene posto in vendita;</li>
                        <li>la data, l’ora e il luogo dell’asta;</li>
                        <li>il prezzo base;</li>
                        <li>il rilancio minimo;</li>
                        <li>il termine di presentazione delle offerte;</li>
                        <li>la modalità di vendita;</li>
                        <li>il sito internet sul quale è pubblicata la relazione di stima.</li>
                    </ul>
                    <p class="JUSTIFIED">
                        <strong>La perizia di stima</strong> è il documento redatto da un esperto estimatore nominato dal Giudice che riporta le caratteristiche intrinseche ed estrinseche del bene, ovvero:
                    </p>
                    <ul class="MARGIN_LEFT_20">

                        <li>valore del bene, con riferimento alle condizioni del mercato immobiliare locale;</li>
                        <li>l’identificazione catastale;</li>
                        <li>la planimetria;</li>
                        <li>le condizioni di fatto, lo stato di manutenzione, la descrizione dettagliata di eventuali problematiche inerenti il bene (ad esempio opere abusive), loro sanabilità e relativi costi;</li>
                        <li>vincoli, servitù, esistenza di debiti verso il condominio e loro entità;</li>
                        <li>stato di possesso del bene.</li>
                    </ul>

                    <p class="JUSTIFIED">
                        <strong>L’ordinanza di vendita</strong> è il documento emesso dal Giudice con il quale quest’ultimo stabilisce:
                    </p>
                    <ul class="MARGIN_LEFT_20">
                        <li>le condizioni e i termini della vendita all’asta;</li>
                        <li>se la stessa debba essere delegata a un professionista;</li>
                        <li>i termini e le modalità del deposito della cauzione;</li>
                        <li>la data ultima per la presentazione delle offerte;</li>
                        <li>l’udienza per la gara tra gli offerenti;</li>
                        <li>il termine entro il quale deve essere versato il pagamento del prezzo residuo;</li>
                        <li>le altre condizioni della vendita.</li>

            </div><!-- end panel heading -->
            <div id="accordion2" class="panel-collapse collapse" style="height: 0px;">
                <div class="panel-body">
                    <p class="JUSTIFIED">L’asta immobiliare giudiziaria, nel processo esecutivo o fallimentare, realizza la vendita di un immobile di proprietà della persona fisica o giuridica, detta esecutato, che subisce l’espropriazione del bene a causa di un debito insoluto nei confronti del creditore. Pertanto un immobile va in asta quando ci troviamo in presenza di un creditore che, munito di titolo esecutivo, come ad esempio un atto di mutuo, si rivolge al Tribunale il quale, nella persone del G.E., invia al debitore un precetto con il quale si dà un’ultima possibilità al debitore di far fronte ai propri debiti. Se il debitore, nel lasso di tempo consentitogli, non adempie a quanto intimatogli dal Giudice, si dà il via alla vera e propria azione esecutiva.</p>

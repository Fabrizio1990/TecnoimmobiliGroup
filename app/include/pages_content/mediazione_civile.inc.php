    <div class="med_intest" style="margin-top:30px;">MEDIAZIONE OBBLIGATORIA</div>
    <p style="margin-right:0px;" class="par_1">
        Con l’entrata in vigore del D.Lgs. n° 28 del 4 marzo 2010 si introduce a partire dal 21 marzo 2011 l’obbligatorietà del tentativo di conciliazione per le seguenti materie (Art. 5)
        (Condominio, Diritti reali, locazione e comodato, affitti di aziende, risarcimento del danno derivante da circolazione stradale e da responsabilità medica, risarcimento danni da
        diffamazione a mezzo stampa o pubblicità, contratti assicurativi o bancari o finanziari).
        <br><br>
        La procedura di mediazione, a partire dal 21 marzo 2011, diventa obbligatoria e rappresenta una "condizione di procedibilità" dell'eventuale successiva azione giudiziaria.
        Se quindi la parte non apre un procedimento di mediazione - con l'invio dell'apposita domanda ad un organismo  accreditato - non può rivolgersi al Giudice.
        <br><br>
        Se invece l'interessato invia la domanda di mediazione ma la controparte non risponde o si rifiuta di aderirvi – ovvero se la mediazione viene tentata ma si conclude con un
        verbale di mancato accordo - questi può sempre successivamente rivolgersi al Giudice per tutelare in sede giudiziaria i propri diritti ed interessi.
        <br><br>
        Di contro, l'altra parte chiamata a partecipare alla procedura di mediazione può scegliere di aderirvi (inviando apposita dichiarazione di adesione) ma è anche libera di
        non parteciparvi. Nel successivo giudizio, però, tale rifiuto, qualora non giustificato, costituirà elemento a suo sfavore ed il Giudice potrà desumere argomento di prova
        dalla mancata adesione.
    </p>




